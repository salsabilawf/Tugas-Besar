    public void tampilkanData(){
        String[] kolom = {"ID", "Nama", "jenis_kelamin","alamat", "email", "no_hp", "Id_kamar","check_in", "check_out"};
        ArrayList<Pemesan> list = new Pemesan().getAll();
        Object rowData[] = new Object[9];
        
        tblPemesan.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(int i = 0; i< list.size(); i++){
            rowData[0] = list.get(i).getId_pemesan();
            rowData[1] = list.get(i).getNama();
            rowData[2] = list.get(i).getJenis_kelamin();
            rowData[3] = list.get(i).getAlamat();
            rowData[4] = list.get(i).getEmail();
            rowData[5] = list.get(i).getNo_hp();
            rowData[6] = list.get(i).getKamar().getId_kamar();
            rowData[7] = list.get(i).getCheck_in();
            rowData[8] = list.get(i).getCheck_out();
            
            ((DefaultTableModel)tblPemesan.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword){
        String[] kolom = {"ID", "Nama", "jenis_kelamin","alamat", "email", "no_hp", "Id_kamar","check_in", "check_out"};
        ArrayList<Pemesan> list = new Pemesan().search(keyword);
        Object rowData[] = new Object[9];
        
        tblPemesan.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        for(Pemesan pem : list){
            rowData[0] = pem.getId_pemesan();
            rowData[1] = pem.getNama();
            rowData[2] = pem.getJenis_kelamin();
            rowData[3] = pem.getAlamat();
            rowData[4] = pem.getEmail();
            rowData[5] = pem.getNo_hp();
            rowData[6] = pem.getKamar().getId_kamar();
            rowData[7] = pem.getCheck_in();
            rowData[8] = pem.getCheck_out();
            
            ((DefaultTableModel)tblPemesan.getModel()).addRow(rowData);
        }
    }
   
