    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Pemesan pem = new Pemesan();
        pem.setId_pemesan(Integer.parseInt(txtId_Pemesan.getText()));
        pem.setNama(txtNama.getText());
        pem.setJenis_kelamin(txtJeniskelamin.getText());
        pem.setAlamat(txtAlamat.getText());
        pem.setEmail(txtEmail.getText());
        pem.setNo_hp(txtNo_hp.getText());
        pem.getKamar().setId_kamar(Integer.parseInt(txtId_kamar.getText()));
        pem.setCheck_in(txtCheck_in.getText());
        pem.setCheck_out(txtCheck_out.getText());
        pem.save();
        txtId_Pemesan.setText(Integer.toString(pem.getId_pemesan()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

